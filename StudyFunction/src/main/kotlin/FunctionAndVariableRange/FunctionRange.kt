    // 함수 안에 또 다른 함수가 선언되어 있는 경우에는 지역 함수(Local Function)라고 한다.

    fun secondFunc(a: Int) { // 지역 함수 선언
        // ...
    }

    userFunc(4) // 사용자 함수 사용 - 선언부의 위치에 상관 없이 사용
